namespace WebRtcVadSharp
{
    /// <summary>
    /// Sample rate of the input audio.
    /// </summary>
    public enum SampleRate
    {
        /// <summary>
        /// 8kHz sample rate.
        /// </summary>
        Is8kHz = 8000,

        /// <summary>
        /// 16kHz sample rate.
        /// </summary>
        Is16kHz = 16000,

        /// <summary>
        /// 32kHz sample rate.
        /// </summary>
        Is32kHz = 32000,

        /// <summary>
        /// 48kHz sample rate.
        /// </summary>
        Is48kHz = 48000,
    }
}
